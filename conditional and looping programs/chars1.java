class chars 
{
	public static void main(String[] args) 
	{
		char[] chars={'b','h','a','v','a','n','i','p','r','a','s','a','d'};
		for (int i=0;i<chars.length ;i++ )
		{
			if (chars[i]=='a'|| chars[i]=='e'||chars[i]=='i'||chars[i]=='o'||chars[i]=='u')
			{
				System.out.println(chars[i]);
			}
		}
	}
}
